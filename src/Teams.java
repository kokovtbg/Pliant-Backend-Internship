import java.util.List;

public class Teams {
    private int n;
    private List<Integer> pnt;
    private List<Integer> pwe;

    public int getN() {
        return n;
    }

    public void setN(int n) {
        this.n = n;
    }

    public List<Integer> getPnt() {
        return pnt;
    }

    public void setPnt(List<Integer> pnt) {
        this.pnt = pnt;
    }

    public List<Integer> getPwe() {
        return pwe;
    }

    public void setPwe(List<Integer> pwe) {
        this.pwe = pwe;
    }
}
